# Нарисовать в консоли ёлку спросив у пользователя количество рядов.
# Пример результата:
# Сколько рядов у ёлки? 5
#     *
#    ***
#   *****
#  *******
# *********

row= int(input("Сколько рядов у ёлки?"))
for i in range(row):
    print(f'{" "*(row-i-1)}{"*"*(2*(i+1)-1)}')





for i in range(2, 10):
    for j in range(2, 11):
        print(f"{i} x {j} = {i*j} ", end ='\t' )
    print()